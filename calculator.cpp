#include <iostream>

int main() {
  int a = 20, b = 10;
  std::cout << "The result of `a + b` is: " << a + b << std::endl;
  std::cout << "The result of `a - b` is: " << a - b << std::endl;
  std::cout << "The result of `a * b` is: " << a * b << std::endl;
  std::cout << "The result of `a / b` is: " << a / b << std::endl;

  return 0;
}
